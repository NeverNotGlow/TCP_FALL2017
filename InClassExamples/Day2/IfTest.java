public class IfTest
{
    public static void main(String[] args)
    {
        int x = 60;
        if(x < 10)
        {
            System.out.println("X is Small!");
        }
        else if(x < 100)
        {
            System.out.println("X is Average!");
        }
        else
        {
            System.out.println("X is Big!");
        }
    }
}